# Integer data type
num1 = 5
num2 = -10
result = num1 + num2
print("Result:", result)


# Float data type
float_num1 = 3.14
float_num2 = -0.001
result = float_num1 * float_num2
print("Result:", result)


# String data type
name = "Alice"
greeting = 'Hello'
message = greeting + ", " + name + "!"
print("Message:", message)


# Boolean data type
is_student = True
is_adult = False
can_vote = is_adult and not is_student
print("Can vote?", can_vote)


# List data type
my_list = [1, 2, 3, "apple", "banana", True]
my_list.append(4)
print("List:", my_list)


# Tuple data type
my_tuple = (1, 2, "apple", "banana", True)
print("Tuple:", my_tuple)


# Dictionary data type
my_dict = {"name": "Alice", "age": 30, "is_student": False}
print("Dictionary:", my_dict)


# Set data type
my_set = {1, 2, 3, 3, 4, 4, 5}  # duplicate elements are automatically removed
print("Set:", my_set)

